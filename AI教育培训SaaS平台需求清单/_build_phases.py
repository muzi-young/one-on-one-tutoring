def read_source():
    wb = openpyxl.load_workbook(SRC, data_only=True)
    ws = wb.active
    header = [c.value for c in ws[1]]
    rows = []
    for i, row in enumerate(ws.iter_rows(min_row=2, max_row=ws.max_row, values_only=True), start=1):
        rows.append({
            'idx': i,
            'port': row[0],
            'section': row[1],
            'func': row[2],
            'desc': row[3] or '',
            'logic': row[4] or '',
            'note': row[5] or '',
        })
    return header, rows


